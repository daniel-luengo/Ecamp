var num = parseInt(prompt("Ingrese un numero del 1 al 4"));

switch(num) {
    case  1:
        alert("Usted ingreso el numero 1");
        break;
    case 2:
        alert("Usted ingreso el numero 2");
        break;
    case 3:
        alert("Usted ingreso el numero 3");
        break;
    case 4:
        alert("Usted ingreso el numero 4");
        break;
        default: alert("No ingreso uno de los numeros pedidos")
}
